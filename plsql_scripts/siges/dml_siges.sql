CREATE OR REPLACE PROCEDURE SP_LISTAR_CENTROS (
    P_ID_UNIDAD_PADRE IN NUMBER,
    P_CURSOR OUT SYS_REFCURSOR
)
AS
BEGIN

    OPEN P_CURSOR FOR

        SELECT  A.IDUNIDADORGANICA,
                A.UORNOMBRE,
                B.UBILOCALIDAD AS DEPARTAMENTO,
                C.UBILOCALIDAD AS PROVINCIA,
                D.UBILOCALIDAD AS DISTRITO
        FROM TGUNIDADORGANICA A
        LEFT JOIN TGUBIGEO B 
            ON SUBSTR(A.UOR_UBIGEO,1,2)||'0000' =
               B.UBIDEPARTAMENTO||B.UBIPROVINCIA||B.UBIDISTRITO

        LEFT JOIN TGUBIGEO C 
            ON SUBSTR(A.UOR_UBIGEO,1,4)||'00' =
               C.UBIDEPARTAMENTO||C.UBIPROVINCIA||C.UBIDISTRITO

        LEFT JOIN TGUBIGEO D 
            ON SUBSTR(A.UOR_UBIGEO,1,6) =
               D.UBIDEPARTAMENTO||C.UBIPROVINCIA||D.UBIDISTRITO

        WHERE (A.UOR_UNIDAD_PADRE = 90
           OR (A.IDUNIDADORGANICA = 90 
               AND A.UOR_UNIDAD_PADRE IS NULL))
        AND A.UORESTADO = 1;
END;
