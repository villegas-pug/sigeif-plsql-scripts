create or replace PROCEDURE USP_ACTUALIZAR_ANEXO_COMPLETO (
    p_id_cabecera       IN NUMBER,
    p_id_anexo          IN NUMBER,
    p_id_centro         IN NUMBER,
    p_fecha_aplicacion  IN DATE,
    p_fecha_registro   IN DATE,   -- ¿ NUEVO
    p_usu_modifica      IN NUMBER,
    p_id_resp_supervision IN NUMBER,
    p_id_director IN NUMBER,
    p_id_supervisado IN NUMBER,
    p_respuestas_json   IN CLOB,
    p_correlativo       OUT NUMBER
)
AS
    v_correlativo NUMBER;
BEGIN
    SELECT CORRELATIVO
    INTO v_correlativo
    FROM SSI_ANEXOS_CABECERA
    WHERE ID_ANEXO_CABECERA = p_id_cabecera;

    UPDATE SSI_ANEXOS_CABECERA
    SET FECHA_APLICACION = p_fecha_aplicacion,
        FECHA_REGISTRO   = p_fecha_registro,   -- ¿ USUARIO
        USU_MODIFICA     = p_usu_modifica,
        FECHA_MODIFICA   = SYSDATE,
        ID_RESP_SUPERVISION = p_id_resp_supervision,
        ID_DIRECTOR = p_id_director,
        ID_SUPERVISADO = p_id_supervisado
    WHERE ID_ANEXO_CABECERA = p_id_cabecera;

    FOR r IN (
        SELECT *
        FROM JSON_TABLE(
            p_respuestas_json,
            '$[*]'
            COLUMNS (
                ap_id_pregunta NUMBER PATH '$.idPregunta',
                ar_respuesta   VARCHAR2(4000) PATH '$.respuesta',
                ar_observacion VARCHAR2(4000) PATH '$.observacion',
                ar_puntaje     NUMBER PATH '$.puntaje'
            )
        )
    ) LOOP
        UPDATE SSI_ANEXOS_RESPUESTAS_V2
        SET AR_RESPUESTA      = r.ar_respuesta,
            AR_OBSERVACION    = r.ar_observacion,
            AR_PUNTAJE        = r.ar_puntaje,
            AR_USU_MODIFICA   = p_usu_modifica,
            AR_FECHA_MODIFICA = SYSDATE
        WHERE ID_ANEXO       = p_id_anexo
          AND ID_CENTRO      = p_id_centro
          AND CORRELATIVO    = v_correlativo
          AND AP_ID_PREGUNTA = r.ap_id_pregunta;
    END LOOP;

    p_correlativo := v_correlativo;
    COMMIT;

EXCEPTION
    WHEN NO_DATA_FOUND THEN
        ROLLBACK;
        RAISE_APPLICATION_ERROR(-20001,
            'No se encontró la cabecera con ID ' || p_id_cabecera);
    WHEN OTHERS THEN
        ROLLBACK;
        RAISE;
END USP_ACTUALIZAR_ANEXO_COMPLETO;

CREATE OR REPLACE PROCEDURE SP_LISTAR_CENTROS (
    P_ID_SERVICIO   IN NUMBER,
    P_TIPO_CENTRO   IN VARCHAR2,
    P_CURSOR        OUT SYS_REFCURSOR
)
AS
    V_ID_UNIDAD_PADRE NUMBER;
BEGIN 
    V_ID_UNIDAD_PADRE :=
        CASE 
            WHEN P_ID_SERVICIO = 4 THEN 73
            WHEN P_ID_SERVICIO IN (5,7,8,10) THEN 90
            WHEN P_ID_SERVICIO = 6 THEN 1118            
        END;

    -- ? CASO 1: SERVICIO 7 ? ESPACIOS
    IF P_ID_SERVICIO IN (7,8, 10) THEN

        OPEN P_CURSOR FOR
        SELECT
            E.ID_ESP_INTERV AS IDUNIDADORGANICA,
            TO_CHAR(E.ESP_NOMBRE) AS UORNOMBRE,

            NULL AS TIPO_CENTRO,

            DEP.UBILOCALIDAD AS DEPARTAMENTO,
            PROV.UBILOCALIDAD AS PROVINCIA,
            DIST.UBILOCALIDAD AS DISTRITO,

            TO_CHAR(E.ESP_NOMBRE_RESPONSABLE) AS DIRECTOR,
            E.ID_PERSONAL AS IDPERSONAL

        FROM SSI_ESP_INTERVENCION E

        LEFT JOIN TGUBIGEO DEP
            ON DEP.UBIDEPARTAMENTO = SUBSTR(E.ESP_UBIGEO,1,2)
           AND DEP.UBIPROVINCIA = '00'
           AND DEP.UBIDISTRITO = '00'

        LEFT JOIN TGUBIGEO PROV
            ON PROV.UBIDEPARTAMENTO = SUBSTR(E.ESP_UBIGEO,1,2)
           AND PROV.UBIPROVINCIA = SUBSTR(E.ESP_UBIGEO,3,2)
           AND PROV.UBIDISTRITO = '00'

        LEFT JOIN TGUBIGEO DIST
            ON DIST.UBIDEPARTAMENTO = SUBSTR(E.ESP_UBIGEO,1,2)
           AND DIST.UBIPROVINCIA = SUBSTR(E.ESP_UBIGEO,3,2)
           AND DIST.UBIDISTRITO = SUBSTR(E.ESP_UBIGEO,5,2)

        WHERE
            E.ESP_ELIMINADO = 0
            AND E.ESP_ESTADO = 1
            AND E.ID_SERVICIO = P_ID_SERVICIO
            AND E.ID_UNIDADORGANICA_PADRE = V_ID_UNIDAD_PADRE

        ORDER BY E.ESP_NOMBRE;

    -- ? CASO 2: SERVICIOS NORMALES
    ELSE

        OPEN P_CURSOR FOR
        SELECT
            A.IDUNIDADORGANICA,
            A.UORNOMBRE,

            CASE
                WHEN A.UORNOMBRE LIKE 'CAR%'
                     AND A.UORNOMBRE NOT LIKE '%ESPECIALIZADO%'
                     AND A.UORNOMBRE NOT LIKE '%URGENCIA%'
                     AND A.IDUNIDADORGANICA NOT IN (124,123,1047,538,534,571,539,154,126,128,155,156)
                THEN 'BÁSICO'

                WHEN A.UORNOMBRE LIKE '%ESPECIALIZADO%'
                     OR A.IDUNIDADORGANICA IN (124,123,1047,538,534,571,539,154,126,128,155,156)
                THEN 'ESPECIALIZADO'

                WHEN A.UORNOMBRE LIKE '%URGENCIA%'
                THEN 'URGENCIA'
            END AS TIPO_CENTRO,

            DEP.UBILOCALIDAD AS DEPARTAMENTO,
            PROV.UBILOCALIDAD AS PROVINCIA,
            DIST.UBILOCALIDAD AS DISTRITO,

            PER.PERNOMBRE || ' ' ||
            PER.PERAPEPATERNO || ' ' ||
            PER.PERAPEMATERNO AS DIRECTOR,

            TP.IDPERSONAL

        FROM TGUNIDADORGANICA A

        LEFT JOIN TGUBIGEO DEP
            ON DEP.UBIDEPARTAMENTO = SUBSTR(A.UOR_UBIGEO,1,2)
           AND DEP.UBIPROVINCIA = '00'
           AND DEP.UBIDISTRITO = '00'

        LEFT JOIN TGUBIGEO PROV
            ON PROV.UBIDEPARTAMENTO = SUBSTR(A.UOR_UBIGEO,1,2)
           AND PROV.UBIPROVINCIA = SUBSTR(A.UOR_UBIGEO,3,2)
           AND PROV.UBIDISTRITO = '00'

        LEFT JOIN TGUBIGEO DIST
            ON DIST.UBIDEPARTAMENTO = SUBSTR(A.UOR_UBIGEO,1,2)
           AND DIST.UBIPROVINCIA = SUBSTR(A.UOR_UBIGEO,3,2)
           AND DIST.UBIDISTRITO = SUBSTR(A.UOR_UBIGEO,5,2)

        LEFT JOIN TRPERSONAL TP
            ON TP.IDPERSONAL = A.UOR_DIRECTOR

        LEFT JOIN TGPERSONA PER
            ON PER.IDPERSONA = TP.PRHPERSONA

        WHERE
            A.UORELIMINADO = 0
            AND A.UORESTADO = 1
            AND A.UOR_TIPO_CENTRO NOT IN (3857)
            AND A.IDUNIDADORGANICA NOT IN (332, 343, 344, 511)

            AND (
                A.UOR_UNIDAD_PADRE = V_ID_UNIDAD_PADRE
                OR (
                    A.IDUNIDADORGANICA = V_ID_UNIDAD_PADRE
                    AND A.UOR_UNIDAD_PADRE IS NULL
                )
            )

            AND (
                P_TIPO_CENTRO = '-1'
                OR P_TIPO_CENTRO IS NULL

                OR (
                    P_TIPO_CENTRO = '1'
                    AND A.UORNOMBRE LIKE 'CAR%'
                    AND A.UORNOMBRE NOT LIKE '%ESPECIALIZADO%'
                    AND A.UORNOMBRE NOT LIKE '%URGENCIA%'
                )

                OR (
                    P_TIPO_CENTRO = '2'
                    AND (
                        A.UORNOMBRE LIKE '%ESPECIALIZADO%'
                        OR A.IDUNIDADORGANICA IN (124,123,1047,538,534,571,539,154,126,128,155,156)
                    )
                )

                OR (
                    P_TIPO_CENTRO = '3'
                    AND A.UORNOMBRE LIKE '%URGENCIA%'
                )
            )

        ORDER BY A.UORNOMBRE;

    END IF;

END;


create or replace PROCEDURE SP_LISTAR_CENTROS (
    P_ID_SERVICIO   IN NUMBER,
    P_TIPO_CENTRO   IN VARCHAR2, -- '1','2','3','-1'
    P_CURSOR        OUT SYS_REFCURSOR
)
AS
    V_ID_UNIDAD_PADRE NUMBER;
BEGIN 
    V_ID_UNIDAD_PADRE :=
        CASE P_ID_SERVICIO
            WHEN 4 THEN 73
            WHEN 5 THEN 90
            WHEN 6 THEN 1118
        END;

    OPEN P_CURSOR FOR

    SELECT
        A.IDUNIDADORGANICA,
        A.UORNOMBRE,

        CASE
            WHEN A.UORNOMBRE LIKE 'CAR%'
                 AND A.UORNOMBRE NOT LIKE '%ESPECIALIZADO%'
                 AND A.UORNOMBRE NOT LIKE '%URGENCIA%'
                 AND A.IDUNIDADORGANICA NOT IN (124,123,1047,538,534,571,539,154,126,128,155,156)
            THEN 'BÁSICO'

            WHEN A.UORNOMBRE LIKE '%ESPECIALIZADO%'
                 OR A.IDUNIDADORGANICA IN (124,123,1047,538,534,571,539,154,126,128,155,156)
            THEN 'ESPECIALIZADO'

            WHEN A.UORNOMBRE LIKE '%URGENCIA%'
            THEN 'URGENCIA'

        END AS TIPO_CENTRO,

        DEP.UBILOCALIDAD AS DEPARTAMENTO,
        PROV.UBILOCALIDAD AS PROVINCIA,
        DIST.UBILOCALIDAD AS DISTRITO,

        PER.PERNOMBRE || ' ' ||
        PER.PERAPEPATERNO || ' ' ||
        PER.PERAPEMATERNO AS DIRECTOR,

        TP.IDPERSONAL

    FROM TGUNIDADORGANICA A

    LEFT JOIN TGUBIGEO DEP
        ON DEP.UBIDEPARTAMENTO = SUBSTR(A.UOR_UBIGEO,1,2)
       AND DEP.UBIPROVINCIA = '00'
       AND DEP.UBIDISTRITO = '00'

    LEFT JOIN TGUBIGEO PROV
        ON PROV.UBIDEPARTAMENTO = SUBSTR(A.UOR_UBIGEO,1,2)
       AND PROV.UBIPROVINCIA = SUBSTR(A.UOR_UBIGEO,3,2)
       AND PROV.UBIDISTRITO = '00'

    LEFT JOIN TGUBIGEO DIST
        ON DIST.UBIDEPARTAMENTO = SUBSTR(A.UOR_UBIGEO,1,2)
       AND DIST.UBIPROVINCIA = SUBSTR(A.UOR_UBIGEO,3,2)
       AND DIST.UBIDISTRITO = SUBSTR(A.UOR_UBIGEO,5,2)

    LEFT JOIN TRPERSONAL TP
        ON TP.IDPERSONAL = A.UOR_DIRECTOR

    LEFT JOIN TGPERSONA PER
        ON PER.IDPERSONA = TP.PRHPERSONA

    WHERE
        A.UORELIMINADO = 0
        AND A.UORESTADO = 1
        AND A.UOR_TIPO_CENTRO NOT IN (3857)
        AND A.IDUNIDADORGANICA NOT IN (332, 343, 344, 511)
        AND (
            A.UOR_UNIDAD_PADRE = V_ID_UNIDAD_PADRE
            OR (
                A.IDUNIDADORGANICA = V_ID_UNIDAD_PADRE
                AND A.UOR_UNIDAD_PADRE IS NULL
            )
        )

        AND (
            P_TIPO_CENTRO = '-1'
            OR P_TIPO_CENTRO IS NULL

            OR (
                P_TIPO_CENTRO = '1' -- BÁSICO
                AND A.UORNOMBRE LIKE 'CAR%'
                AND A.UORNOMBRE NOT LIKE '%ESPECIALIZADO%'
                AND A.UORNOMBRE NOT LIKE '%URGENCIA%'
            )

            OR (
                P_TIPO_CENTRO = '2' -- ESPECIALIZADO
                AND (
                    A.UORNOMBRE LIKE '%ESPECIALIZADO%'
                    OR A.IDUNIDADORGANICA IN (124,123,1047,538,534,571,539,154,126,128,155,156)
                )
            )

            OR (
                P_TIPO_CENTRO = '3' -- URGENCIA
                AND A.UORNOMBRE LIKE '%URGENCIA%'
            )

        )
 UNION ALL
 
  SELECT
        E.ID_ESP_INTERV AS IDUNIDADORGANICA,
        TO_CHAR(E.ESP_NOMBRE) AS UORNOMBRE,

        'ESPACIO' AS TIPO_CENTRO,

        DEP.UBILOCALIDAD AS DEPARTAMENTO,
        PROV.UBILOCALIDAD AS PROVINCIA,
        DIST.UBILOCALIDAD AS DISTRITO,

        TO_CHAR(E.ESP_NOMBRE_RESPONSABLE) AS DIRECTOR,

        E.ID_PERSONAL AS IDPERSONAL

    FROM SSI_ESP_INTERVENCION E

    LEFT JOIN TGUBIGEO DEP
        ON DEP.UBIDEPARTAMENTO = SUBSTR(E.ESP_UBIGEO,1,2)
       AND DEP.UBIPROVINCIA = '00'
       AND DEP.UBIDISTRITO = '00'

    LEFT JOIN TGUBIGEO PROV
        ON PROV.UBIDEPARTAMENTO = SUBSTR(E.ESP_UBIGEO,1,2)
       AND PROV.UBIPROVINCIA = SUBSTR(E.ESP_UBIGEO,3,2)
       AND PROV.UBIDISTRITO = '00'

    LEFT JOIN TGUBIGEO DIST
        ON DIST.UBIDEPARTAMENTO = SUBSTR(E.ESP_UBIGEO,1,2)
       AND DIST.UBIPROVINCIA = SUBSTR(E.ESP_UBIGEO,3,2)
       AND DIST.UBIDISTRITO = SUBSTR(E.ESP_UBIGEO,5,2)

    WHERE
        E.ESP_ELIMINADO = 0
        AND E.ESP_ESTADO = 1
        AND E.ID_SERVICIO = P_ID_SERVICIO
        AND E.ID_UNIDADORGANICA_PADRE = V_ID_UNIDAD_PADRE

    ORDER BY A.UORNOMBRE;

END SP_LISTAR_CENTROS;



