create or replace PROCEDURE USP_LISTAR_ANEXOS_POR_FILTRO (
    P_ID_UNIDAD_ORGANICA IN NUMBER DEFAULT NULL,
    P_ID_SERVICIO        IN NUMBER DEFAULT NULL, -- este será SI_ID_SERVICIO2
    P_ID_ANEXO           IN NUMBER DEFAULT NULL,
    C_RESULTADO          OUT SYS_REFCURSOR
)
AS
BEGIN
    OPEN C_RESULTADO FOR
        SELECT DISTINCT
            A.ID_ANEXO             AS idAnexo,
            A.ANX_NOMBRE           AS nombreAnexo,
            A.ANX_ID_SERVICIO      AS idServicio,
            A.ANX_SERVICIO         AS nombreServicio,
            A.IDUNIDADORGANICA     AS idUnidad,
            A.ANX_UNIDAD_ORGANICA  AS nombreUnidad,
            A.ANX_CODIGO2           AS codigoAnexo2
        FROM SSI_ANEXO A
        INNER JOIN SSI_ANEXOS_PREGUNTAS P
            ON P.AP_NUM_ANEXO = A.ID_ANEXO
           AND NVL(P.AP_ELIMINADO,0) = 0
        WHERE NVL(A.ANX_ELIMINADO, 0) = 0

        AND (P_ID_UNIDAD_ORGANICA IS NULL 
             OR A.IDUNIDADORGANICA = P_ID_UNIDAD_ORGANICA)

        -- ¿ AQUI USAMOS SI_ID_SERVICIO2
        AND (P_ID_SERVICIO IS NULL 
             OR P.SI_ID_SERVICIO2 = P_ID_SERVICIO)

        AND (P_ID_ANEXO IS NULL 
             OR A.ID_ANEXO = P_ID_ANEXO)

        ORDER BY
            A.ANX_UNIDAD_ORGANICA,
            A.ANX_SERVICIO,
            A.ANX_NOMBRE;

END;


