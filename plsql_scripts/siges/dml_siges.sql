SELECT
        uor.IDUNIDADORGANICA AS id_centro,
        uor.UORNOMBRE        AS nombre_centro
        FROM TGUNIDADORGANICA uor
        WHERE uor.UORELIMINADO = 0
        AND uor.UORESTADO   = 1
        AND uor.UOR_TIPO_CENTRO NOT IN (3926,3857)
        AND uor.UOR_UNIDAD_PADRE = 73
        AND (
             CASE
                WHEN REGEXP_SUBSTR(:P_VALOR, '([^|]*)(\||$)', 1, 1, NULL, 1) = '-1' THEN 1
                WHEN REGEXP_SUBSTR(:P_VALOR, '([^|]*)(\||$)', 1, 1, NULL, 1) IS NULL
                     OR REGEXP_SUBSTR(:P_VALOR, '([^|]*)(\||$)', 1, 1, NULL, 1) = '' THEN 1
                WHEN REGEXP_SUBSTR(:P_VALOR, '([^|]*)(\||$)', 1, 1, NULL, 1) = '1'
                     AND (uor.UORNOMBRE LIKE 'CAR%'
                      AND uor.UORNOMBRE NOT LIKE '%ESPECIALIZADO%'
                      AND uor.UORNOMBRE NOT LIKE '%URGENCIA%'
                      AND uor.IDUNIDADORGANICA NOT IN (124,123,1047,538,534,571,539,154,126,128,155,156)
                     ) THEN 1
                WHEN REGEXP_SUBSTR(:P_VALOR, '([^|]*)(\||$)', 1, 1, NULL, 1) = '2'
                     AND (uor.UORNOMBRE LIKE '%ESPECIALIZADO%'
                      OR uor.IDUNIDADORGANICA IN (124,123,1047,538,534,571,539,154,126,128,155,156)
                     ) THEN 1
                WHEN REGEXP_SUBSTR(:P_VALOR, '([^|]*)(\||$)', 1, 1, NULL, 1) = '3'
                     AND (uor.UORNOMBRE LIKE '%URGENCIA%') THEN 1
                ELSE 0
            END
          ) = 1
    ORDER BY uor.UORNOMBRE;



SELECT
           uor.IDUNIDADORGANICA AS id,
           uor.UORNOMBRE        AS nombre,
          -- SUBSTR(uor.UORNOMBRE,1,3) AS tipo,
           CASE
            WHEN SUBSTR(TO_CHAR(uor.UORNOMBRE), 1, 3) = 'CED' THEN 'CEDIF'
             ELSE SUBSTR(TO_CHAR(uor.UORNOMBRE), 1, 3)
            END AS TIPO,
               /* Tipo de CAR (solo aplica a CAR) */
            CASE
                WHEN uor.UORNOMBRE LIKE 'CAR %' THEN
                    CASE
                        WHEN uor.UORNOMBRE LIKE '%URGENCIA%' THEN 'CAR URGENCIA'
                        WHEN uor.UORNOMBRE LIKE '%ESPECIALIZADO%'
                             OR uor.IDUNIDADORGANICA IN
                                (124,123,1047,538,534,571,539,154,126,128,155,156)
                             THEN 'CAR ESPECIALIZADO'
                        ELSE 'CAR BÁSICO'
                    END
                ELSE NULL
            END AS TIPO_CAR,
           B.UBILOCALIDAD       AS departamento,
           C.UBILOCALIDAD       AS provincia,
           D.UBILOCALIDAD       AS distrito,
           uor.UORDIRECCION     AS direccion,
