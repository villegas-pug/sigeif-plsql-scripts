    P_ID_ANEXO_CABECERA IN NUMBER,
    P_CORRELATIVO       IN NUMBER,
    P_CURSOR            OUT SYS_REFCURSOR
)
AS
BEGIN
    OPEN P_CURSOR FOR

    WITH RESPUESTA_UNICA AS (
        SELECT *
        FROM (
            SELECT R.*,
                   ROW_NUMBER() OVER (
                       PARTITION BY R.ID_ANEXO,
                                    R.ID_CENTRO,
                                    R.CORRELATIVO,
                                    R.AP_ID_PREGUNTA
                       ORDER BY R.AR_FECHA_REGISTRA DESC
                   ) RN
            FROM SSI_ANEXOS_RESPUESTAS_V2 R
        )
        WHERE RN = 1
    )

    SELECT 
        AC.ID_ANEXO_CABECERA,
        A.ANX_NOMBRE          AS NOMBRE_ANEXO,
        A.ANX_CODIGO          AS CODIGO_ANEXO,
        A.ANX_SERVICIO        AS NOMBRE_SERVICIO,
        U.UORNOMBRE           AS NOMBRE_CENTRO,
        A.ANX_UNIDAD_ORGANICA AS NOMBRE_UNIDAD,
        B.UBILOCALIDAD        AS DEPARTAMENTO,
        C.UBILOCALIDAD        AS PROVINCIA,
        D.UBILOCALIDAD        AS DISTRITO,
        AC.FECHA_APLICACION,
        AC.FECHA_REGISTRO,   -- ¿ AGREGADO
        AC.CORRELATIVO,

        P.AP_ID_PREGUNTA,
        P.AP_TIPO_CONTROL     AS TIPO_CONTROL,
        P.AP_PREGUNTA         AS PREGUNTA,
        P.AP_OPCIONES         AS OPCIONES,
        P.AP_OBLIGATORIA1    AS OBLIGATORIA,
        P.AP_PREGUNTA2       AS PREGUNTA2,
        P.AP_TIPO_CONTROL2   AS TIPO_CONTROL2,
        P.AP_OPCIONES2      AS OPCIONES2,
        P.AP_OBLIGATORIA2   AS OBLIGATORIA2,
        RU.AR_RESPUESTA       AS RESPUESTA,
        AC.AUDIO_URL,
        PER.PERNOMBRE || ' ' ||
        PER.PERAPEPATERNO || ' ' ||
        PER.PERAPEMATERNO AS DIRECTOR,
        CASE
            WHEN U.UORNOMBRE LIKE 'CAR%'
                 AND U.UORNOMBRE NOT LIKE '%ESPECIALIZADO%'
                 AND U.UORNOMBRE NOT LIKE '%URGENCIA%'
            THEN 'BÁSICO'
        
            WHEN U.UORNOMBRE LIKE '%ESPECIALIZADO%'
            THEN 'ESPECIALIZADO'
        
            WHEN U.UORNOMBRE LIKE '%URGENCIA%'
            THEN 'URGENCIA'
        
            ELSE NULL
        END AS TIPO_CENTRO,
        AC.ID_RESP_SUPERVISION,
        PER_RESP.PERNOMBRE || ' ' ||
        PER_RESP.PERAPEPATERNO || ' ' ||
        PER_RESP.PERAPEMATERNO AS RESP_SUPERVISION,
        AC.ID_SUPERVISADO,
        PER_SUP.PERNOMBRE || ' ' ||
        PER_SUP.PERAPEPATERNO || ' ' ||
        PER_SUP.PERAPEMATERNO AS SUPERVISADO,
        
        A.ANX_CODIGO2          AS CODIGO_ANEXO2,
        P.AP_TIPO_DATO1 AS TIPODATO1,
        P.AP_TIPO_DATO2 AS TIPODATO2,
        P.AP_CONDICION AS CONDICION

    FROM SSI_ANEXOS_CABECERA AC

    LEFT JOIN SSI_ANEXO A
        ON AC.ID_ANEXO = A.ID_ANEXO

    LEFT JOIN TGUNIDADORGANICA U
        ON AC.ID_CENTRO = U.IDUNIDADORGANICA

    LEFT JOIN TGUBIGEO B
        ON SUBSTR(U.UOR_UBIGEO,1,2)||'0000' =
           B.UBIDEPARTAMENTO||B.UBIPROVINCIA||B.UBIDISTRITO

    LEFT JOIN TGUBIGEO C
        ON SUBSTR(U.UOR_UBIGEO,1,4)||'00' =
           C.UBIDEPARTAMENTO||C.UBIPROVINCIA||C.UBIDISTRITO

    LEFT JOIN TGUBIGEO D
        ON SUBSTR(U.UOR_UBIGEO,1,6) =
           D.UBIDEPARTAMENTO||D.UBIPROVINCIA||D.UBIDISTRITO

    LEFT JOIN TRPERSONAL TP
        ON TP.IDPERSONAL = U.UOR_DIRECTOR

    LEFT JOIN TGPERSONA PER
        ON PER.IDPERSONA = TP.PRHPERSONA
    
    LEFT JOIN TRPERSONAL TP_RESP
    ON TP_RESP.IDPERSONAL = AC.ID_RESP_SUPERVISION

    LEFT JOIN TGPERSONA PER_RESP
        ON PER_RESP.IDPERSONA = TP_RESP.PRHPERSONA
    
    LEFT JOIN TRPERSONAL TP_SUP
        ON TP_SUP.IDPERSONAL = AC.ID_SUPERVISADO
    
    LEFT JOIN TGPERSONA PER_SUP
    ON PER_SUP.IDPERSONA = TP_SUP.PRHPERSONA

    INNER JOIN SSI_ANEXOS_PREGUNTAS P
        ON A.ID_ANEXO = P.AP_NUM_ANEXO       

    INNER JOIN RESPUESTA_UNICA RU
        ON RU.ID_ANEXO       = AC.ID_ANEXO
       AND RU.ID_CENTRO      = AC.ID_CENTRO
       AND RU.CORRELATIVO    = AC.CORRELATIVO
       AND RU.AP_ID_PREGUNTA = P.AP_ID_PREGUNTA

    WHERE AC.ID_ANEXO_CABECERA = P_ID_ANEXO_CABECERA
      AND AC.CORRELATIVO       = P_CORRELATIVO

    ORDER BY P.AP_NUM_GRUPO, P.AP_NUM_PREGUNTA;

END SP_OBTENER_ANEXO_CABECERA;
commit;

