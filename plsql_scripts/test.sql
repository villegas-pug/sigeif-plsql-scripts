                            LEFT JOIN TRPERSONAL ps2 on ps2.IDPERSONAL = eq2.PER_ID_PERSONAL   
                            LEFT JOIN TGPERSONA pe2 on pe2.IDPERSONA = ps2.PRHPERSONA
                            WHERE EQ_ID_EQUIPO = pf.EQ_ID_EQUIPO
                        )  
                     WHEN pt.PT_ID_POBLACION = 5840 AND pf.EQ_ID_EQUIPO IS  NULL  THEN           
                        (
                            SELECT 
                            pe2.PERNOMBRE || ' ' || pe2.PERAPEPATERNO || ' ' || pe2.PERAPEMATERNO 
                            FROM TRPERSONAL ps2 
                            LEFT JOIN TGPERSONA pe2 on pe2.IDPERSONA = ps2.PRHPERSONA
